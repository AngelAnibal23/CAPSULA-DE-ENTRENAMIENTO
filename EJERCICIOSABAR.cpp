/*
Escriba un programa recursivo que invierta el orden de los elemntos de un arreglo de n elementos.
 Es decir que el elemento que esta en a posicion 1 se intercambie con el qu esta en la posicion n, 
el de posicion 2 con el de posicion n-1 y asi sucesivamente. 
*/

#include <iostream> 
using namespace std; 

int main(){
	int n; 
	
	cin>>n; 
	
	int a[n]; 
	
	for(int i=0; i<n; i++){
		cin>>a[i]; 
	}
	
	
	
	return 0; 
}